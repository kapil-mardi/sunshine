package kapsapps.xyz.sunshine.model;

public class CloudInfo{
    private int all;

    public int getAll() {
        return all;
    }

    public void setAll(int all) {
        this.all = all;
    }

    @Override
    public String toString() {
        return "CloudInfo{" +
                "all=" + all +
                '}';
    }
}

